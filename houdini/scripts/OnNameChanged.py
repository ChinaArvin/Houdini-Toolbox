"""Perform tasks when a Houdini node has its name changed."""

# =============================================================================
# IMPORTS
# =============================================================================

# Houdini Toolbox Imports
from ht.events import NodeEvents, runEvent

# =============================================================================
# FUNCTIONS
# =============================================================================

def main():
    """Main function."""
    runEvent(NodeEvents.OnNameChanged, kwargs)

# =============================================================================

main()

