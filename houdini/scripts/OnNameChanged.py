"""Perform tasks when a Houdini node has its name changed."""

# =============================================================================
# IMPORTS
# =============================================================================

# Houdini Toolbox Imports
import ht.nodes.colors

# =============================================================================
# FUNCTIONS
# =============================================================================

def main():
    """Main function."""
    node = kwargs["node"]

    ht.nodes.colors.colorNodeByName(node)

# =============================================================================

main()

